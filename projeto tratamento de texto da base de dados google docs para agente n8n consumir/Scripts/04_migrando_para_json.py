base_agrupada = []

topico_atual = None
subtopico_atual = None
contador_topico = 1


for linha in texto.strip().splitlines():

    linha = linha.strip()

    if not linha:
        continue

    linha = corrigir_hifens(linha)

    if eh_topico(linha):

        topico_atual = {
            "id": contador_topico,
            "codigo": extrair_codigo(linha),
            "titulo": formatar_titulo_topico(linha),
            "subtopicos": []
        }

        base_agrupada.append(topico_atual)

        contador_topico += 1

        contador_subtopico = 1

        subtopico_atual = None

    elif eh_subtopico(linha):

        if topico_atual is None:
            continue

        subtopico_atual = {
            "id": contador_subtopico,
            "codigo": extrair_codigo(linha),
            "titulo": formatar_titulo_subtopico(linha),
            "palavras_chave": [],
            "resumo": "",
            "conteudo": []
        }

        topico_atual["subtopicos"].append(subtopico_atual)

        contador_subtopico += 1

    else:

        if subtopico_atual is not None:
            subtopico_atual["conteudo"].append(limpar_conteudo(linha))

