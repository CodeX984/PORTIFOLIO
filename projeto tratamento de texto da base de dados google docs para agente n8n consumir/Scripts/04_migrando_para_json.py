# =========================
# 2. REGEX
# =========================

regex_topico = re.compile(r'^\d+\s-\s.+$')
regex_subtopico = re.compile(r'^\d+\.\d+\s-\s.+$')
regex_conteudo = re.compile(r'^\d+(?:\.\d+){2,}\s-\s.+$')

def corrigir_hifens(texto):
    texto = texto.strip()

    texto = texto.replace("–", "-")
    texto = texto.replace("—", "-")
    texto = texto.replace("−", "-")

    texto = re.sub(r'^(\d+(?:\.\d+)*)\s*-\s*-\s*', r'\1 - ', texto)
    texto = re.sub(r'^(\d+(?:\.\d+)*)\s+(?!-)(.+)$', r'\1 - \2', texto)
    texto = re.sub(r'^(\d+(?:\.\d+)*)\s*-\s*(.+)$', r'\1 - \2', texto)

    texto = re.sub(r'\s+', ' ', texto)

    return texto.strip()


def eh_topico(linha):
    return bool(regex_topico.match(linha.strip()))


def eh_subtopico(linha):
    return bool(regex_subtopico.match(linha.strip()))


def eh_conteudo(linha):
    return bool(regex_conteudo.match(linha.strip()))


def extrair_codigo(linha):
    return linha.split(" - ", 1)[0].strip()


def extrair_titulo(linha):
    partes = linha.split(" - ", 1)

    if len(partes) != 2:
        return linha.strip()

    return partes[1].strip()


def formatar_titulo_topico(linha):
    return extrair_titulo(linha).upper()


def formatar_titulo_subtopico(linha):
    return extrair_titulo(linha).title()


def limpar_conteudo(linha):
    linha = linha.strip()
    linha = re.sub(r'^\d+(?:\.\d+)+\s-\s', '', linha)
    linha = re.sub(r'\s+', ' ', linha)
    return linha.strip()


def exportar_json(dados, nome_arquivo="base_conhecimento_agrupada.json", baixar=True):
    with open(nome_arquivo, "w", encoding="utf-8") as f:
        json.dump(dados, f, ensure_ascii=False, indent=4)

    print(f"JSON salvo: {nome_arquivo}")
    print(f"Tópicos: {len(dados)}")

    total_subtopicos = sum(len(topico["subtopicos"]) for topico in dados)
    print(f"Subtópicos: {total_subtopicos}")

    if baixar:
        files.download(nome_arquivo)

# =========================
# 3. GERAR ESTRUTURA
